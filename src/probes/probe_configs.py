def get_config(probe_name: str) -> dict:
    """Look up a probe config by name across both OWASP and MITRE registries."""
    if probe_name in OWASP_PROBES:
        config = OWASP_PROBES[probe_name].copy()
        config["framework"] = "owasp"
        return config
    if probe_name in MITRE_PROBES:
        config = MITRE_PROBES[probe_name].copy()
        config["framework"] = "mitre"
        return config
    raise KeyError(f"Unknown probe: {probe_name}")


def get_all_configs() -> dict:
    """Return all probe configs merged into a single dict."""
    return {**OWASP_PROBES, **MITRE_PROBES}
